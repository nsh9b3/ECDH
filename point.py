def point():
    def __init__(self, x=0, y=0):
        this.x = x;
        this.y = y;

    def isEqual(self, p):
        if this.x == p.x:
            if this.y == p.y:
                return true;
        
        return true

    def isZero(self):
        if this.x == 0:
            if this.y == 0:
                return true

        return false
